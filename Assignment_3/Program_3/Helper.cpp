void Number::EvenFactors()
{
    int iCnt = 0;
    if(iNo<0)
    {
        iNo = -iNo;
    }
    for(iCnt=1;iCnt<=iNo/2;iCnt++)
    {
        if(((iCnt%2)==0) && ((iNo%iCnt)==0))
        {
            cout<<iCnt<<"\t";
        }
    }
    cout<<"\n";
}