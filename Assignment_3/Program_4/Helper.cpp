void Character::DisplayChar()
{
    if(((ch>='a') && (ch<='z')) || ((ch>='A') && (ch<='Z')))
    {
        cout<<ch<<"\n";
    }
    else
    {
        cout<<ch<<" is not an alphabate\n";
    }
    
}