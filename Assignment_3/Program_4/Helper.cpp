///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : ConvertCase
// Parameters    : None
// Return Value  : None
// Description   : It is used to convert the case of the character.
// Author        : Swapnil Ramesh Adhav
// Date          : 15 Nov 2020
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Character::ConvertCase()
{
    if((ch>='A') && (ch<='Z'))
    {
        ch = ch + 32;
    }
    else if((ch>='a') && (ch<='z'))
    {
        ch = ch - 32;
    }
    else
    {
        return;
    }
    
}

